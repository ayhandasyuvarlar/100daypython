# Basic arithmetic operators
print(3 + 2)  # Output: 5
print(3 - 2)  # Output: 1
print(3 * 2)  # Output: 6
print(3 / 2)  # Output: 1.5
print(3 // 2)  # Output: 1
print(3 % 2)  # Output: 1
print(3 ** 2)  # Output: 9

# Order of operations
print(3 + 2 * 4 - 1)
# 3 + 2 * 4 - 1
#  = 3 + 8 - 1
#  = 11 - 1
#  = 10

# Using parentheses to specify the order of operations
print((3 + 2) * 4 - 1)
# (3 + 2) * 4 - 1
#  = 5 * 4 - 1
#  = 20 - 1
#  = 19


# Assigning values to variables
x = 3
y = 2
print(x + y)  # Output: 5
print(x * y)  # Output: 6

